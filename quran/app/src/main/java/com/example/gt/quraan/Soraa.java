public class Soraa {
    private String name;
    private int pageNumber;

    public Soraa(String name, int page) {
        this.name = name;
        this.pageNumber = page;
    }

    public String getName() {
        return name;
    }

    public int getPageNumber() {
        return pageNumber;
    }
}
